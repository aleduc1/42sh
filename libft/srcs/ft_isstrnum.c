#include "libft.h"

int		ft_isstrnum(char *s)
{
	if (s)
	{
		if (*s == '-')
			s++;
		while (*s)
		{
			if (!(ft_isdigit(*s)))
				return (0);
			s++;
		}
		return (1);
	}
	return (0);
}
