#include "lexer.h"

int		dub_possible(char c)
{
	if (c == '|' || c == '&' || c == '>' || c == '<' || c == ';')
		return (1);
	return (0);
}

int		ft_isreallyspace(char c)
{
	if (c == ' ' || c == '\t' || c == '\v'
			|| c == '\f' || c == '\r')
		return (1);
	return (0);
}
